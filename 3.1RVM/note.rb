command cơ bản:
rvm --ruby-version use [version]: tạo file .ruby-version cho project
rvm --ruby-version use [version]@gemname: tạo đồng thời cả .ruby-version và  ruby-gemset 
rvm gemset create [name] tạo gemset cho ruby version hiện tại
rvm gemset use/delete [name]
rvm gemset list : list gemset 
rvm list : list ruby 
rvm use [version]: chuyển version ruby 
ruby -v: check version ruby 
rvm install [version]